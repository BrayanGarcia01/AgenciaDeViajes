    private void btnAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarUsuarioActionPerformed
        //Valido el nombre
        String v_nombre=" ";

        if(txtNombreAU.getText().length()==0){
                Utilidades.mensajeInformacion("Digite un nombre, por favor", "Informacion");
                return;
            }
        v_nombre=txtNombreAU.getText();        
        //Valido la contrasenia
        if(txtContraseniaAU.getText().length()==0){
            Utilidades.mensajeInformacion("Es obligatorio ingresar una contrase√±a", "Informacion");
            return;
        }
        if(!validarContraseniaUsuario(txtContraseniaAU.getText())){
            return;
        }
        String v_contrasenia=txtContraseniaAU.getText();
        //Valido nombre completo
        if(txtNombreCompletoAU.getText().length()==0){
            Utilidades.mensajeInformacion("Es obligatorio ingresar el nombre completo", "Informacion");
            return;
        }
        String v_nombreCompleto=txtNombreCompletoAU.getText();
        
        //Valido que se escogio un valor para el cargo
        int v_cargo=0;
        //10 = Administrador, 20=Funcionario
        if(rbtnAdministradorAU.isSelected()){
            v_cargo=10;
        }else if(rbtnFuncionarioAU.isSelected()){
            v_cargo=20;
        }else{
            Utilidades.mensajeInformacion("Escoga un cargo", "Informacion");
            return;
        }
        
        //Creo y agreggo un usuario dependiendo del cargo escogido (Administrador o funcionario)
        try{
            boolean agregado=false;
            if(v_cargo==10){
                Administrador nuevoUsuario=new Administrador(v_nombre, v_contrasenia, v_nombreCompleto, null);
                agregado = administrador.agregarUsuario(nuevoUsuario);

            }        
            if(v_cargo==20){
                Funcionario nuevoUsuario=new Funcionario(v_nombre, v_contrasenia, v_nombreCompleto, null);
                agregado = administrador.agregarUsuario(nuevoUsuario);
            }


            if(agregado){
                Utilidades.mensajeExito("Se agrego el Usuario", "Agregacion Exitosa");
                limpiarCamposAgregarUsuario();
            }else{
                Utilidades.mensajeError("El Usuario a agregar ya existe", "Error");
            }

        }catch (IOException ex) {
            Logger.getLogger(Funcionario.class.getName()).log(Level.SEVERE, null, ex);
            Utilidades.mensajeAdvertencia("Se ha interrumpido la conexion con el servidor", "Error");
        }
        
    }//GEN-LAST:event_btnAgregarUsuarioActionPerformed

